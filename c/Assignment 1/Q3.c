#include<stdio.h>
#include<stdlib.h>

int main()
{
    float radius,diameter,area,pi,circumference;
    pi = 3.14159;
    printf("Enter the value for radius \n");
    scanf("%f",&radius);

    printf("Diameter of the circle is %f \n",diameter=2*radius);
    printf("The value of the Circumference of circle is %f \n",circumference=2*pi*radius);
    printf("The Area of circle is %f \n",area=pi*radius*radius);
    
}

